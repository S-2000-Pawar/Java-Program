//find area and perimeter of a rectangle


class Rectangle
{
    public static void main(String[] arg)
    {
       int l=30;
       int w=20;
       int area=l*w;
       int perimeter=2*(l+w);
       System.out.println("area of rectangle" +area);
       System.out.println("perimeter of rectangle" +perimeter);
   }
}
