//to display the operation of pre and post increment and decrement


class Increment
{
   public static void main(String[] args)
   {
      int a=57;
      System.out.println(a);
      System.out.println("Increment");
      System.out.println(a++);
      System.out.println(++a);
      System.out.println(a+=1);
      System.out.println("Decrement");
      System.out.println(a--);
      System.out.println(--a);
      System.out.println(a-=1);
      
      
      
      
   }
}