//to display various type or arithmatic operation using mixed data type


class Arithmatic
{
    public static void main(String[] args)
    {
      int x=5,y=7;
      double z=3.7,w=8.0,s;
      s=x+y;
      System.out.println("Addition of number"+s);
      s=z+w;
      System.out.println("Addition of number"+s);
      s=x+w;
      System.out.println("Addition of number"+s);
      s=x-y;
      System.out.println("Substraction of number"+s);
      s=z-w;
      System.out.println("Substraction of number"+s);
      s=x*y;
      System.out.println("Multiplication of number"+s);
      s=z*w;
      System.out.println("Multiplication of number"+s);
      s=x*w;
      System.out.println("Multiplication of number"+s);
      s=x/y;
      System.out.println("Division of number"+s);
      s=z/w;
      System.out.println("Division of number"+s);
      s=x/w;
      System.out.println("Division of number"+s);
    }
}
      
      
      
