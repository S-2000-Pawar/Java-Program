//Area and  circumference of a circle


class Circle
{
   public static void main(String[] arg)
   {
     int r=12;
     double pie=3.14;
     double area=pie*r*r;
     double circum=2*pie*r;
     System.out.println("area of circle" +area);
     System.out.println("circumference of circle" +circum);
  }
}
   